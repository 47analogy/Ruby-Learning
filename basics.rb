# example of number, string, and boolean data types

data_num =  47   

data_boolean =  true   

data_string =  "fantastic!"   

# puts adds a blank line (vs. print --just outputs to screen)
puts data_num
puts data_boolean
puts data_string


# methods

# .length
day_string = 'Tuesday'
puts day_string.length # <-- 7

# .reverse
rev_string = 'desrever'
puts rev_string.reverse # <-- 'reversed'

# .upcase and .downcase
puts day_string.upcase
puts 'LAND MASSES'.downcase